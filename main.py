print("******* Area of a Circle Calculator ******* \n");

radius = int(input("Enter the radius of a circle:")); #change the input string into integer
Area = round(3.142*(radius)**2 , 2);   #round into 2 decimals

print(f"\n Area of a circle is {Area} squared units.\n");

